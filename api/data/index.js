// ============================================================
// Data API — Full CRUD for admin products/categories/config
// Stores the entire admin data set as a single JSONB row in NeonDB
// GET  /api/data          → returns JSON { success, data }
// GET  /api/data?format=js → returns JavaScript (like products-data.js)
// POST /api/data          → saves data { data: {...} }
// ============================================================
const { neon } = require('@neondatabase/serverless');
const fs = require('fs');
const path = require('path');

// ── Helpers ───────────────────────────────────────────────────
function jsonResponse(res, data, status = 200) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'GET, POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
  return res.status(status).json(data);
}

function getDb() {
  const connectionString = process.env.DATABASE_URL || process.env.POSTGRES_URL;
  if (!connectionString) throw new Error('DATABASE_URL environment variable is required');
  return neon(connectionString);
}

async function ensureTable(sql) {
  await sql`
    CREATE TABLE IF NOT EXISTS admin_data (
      id INTEGER PRIMARY KEY DEFAULT 1,
      data JSONB NOT NULL DEFAULT '{}',
      updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
      CONSTRAINT single_row CHECK (id = 1)
    );
  `;
  const rows = await sql`SELECT id, data FROM admin_data WHERE id = 1`;
  if (rows.length === 0) {
    await seedFromStaticFile(sql);
  }
}

function tryReadStaticData() {
  const base = process.cwd();
  const candidates = [
    path.join(base, 'data', 'products-data.js'),
    path.join(base, 'products-data.js'),
    path.join(base, '_products-data.js'),
  ];
  for (const p of candidates) {
    if (fs.existsSync(p)) {
      try {
        const content = fs.readFileSync(p, 'utf-8');
        const match = content.match(/var productsData\s*=\s*(\{[\s\S]*\});/);
        if (match) {
          const data = JSON.parse(match[1]);
          console.log('Read static data from', path.relative(base, p));
          return data;
        }
      } catch (e) {
        console.error('Failed to parse', p, e.message);
      }
    }
  }
  return null;
}

async function seedFromStaticFile(sql) {
  const seedData = tryReadStaticData() || {};
  await sql`INSERT INTO admin_data (id, data) VALUES (1, ${JSON.stringify(seedData)}::jsonb)`;
}

function isDataCorrupted(data) {
  return !data || !Array.isArray(data.products) || data.products.length === 0;
}

// ── Serve as JavaScript (products-data.js format) ──────────────
async function serveAsJavaScript(sql, res) {
  try {
    // Auto-repair: reseed if data corrupted
    const rows = await sql`SELECT data FROM admin_data WHERE id = 1`;
    if (rows.length > 0 && rows[0].data) {
      const dbData = rows[0].data;
      if (isDataCorrupted(dbData)) {
        console.log('serveAsJavaScript: Data corrupted, reseeding');
        await sql`DELETE FROM admin_data WHERE id = 1`;
        await seedFromStaticFile(sql);
        const newRows = await sql`SELECT data FROM admin_data WHERE id = 1`;
        if (newRows.length > 0 && newRows[0].data && !isDataCorrupted(newRows[0].data)) {
          const data = newRows[0].data;
          let js = `// Auto-generated by API - ${new Date().toISOString()}\n`;
          js += `var productsData = ${JSON.stringify(data, null, 2)};\n`;
          res.setHeader('Content-Type', 'application/javascript');
          res.setHeader('Cache-Control', 'no-cache, no-store, must-revalidate');
          return res.status(200).send(js);
        }
      }
      if (!isDataCorrupted(dbData)) {
        const data = dbData;
        let js = `// Auto-generated by API - ${new Date().toISOString()}\n`;
        js += `var productsData = ${JSON.stringify(data, null, 2)};\n`;
        res.setHeader('Content-Type', 'application/javascript');
        res.setHeader('Cache-Control', 'no-cache, no-store, must-revalidate');
        return res.status(200).send(js);
      }
    }
  } catch (e) {
    console.error('DB read error, falling back to static file:', e.message);
  }

  // Fallback: serve the static products-data.js
  const staticPath = path.join(process.cwd(), 'products-data.js');
  if (fs.existsSync(staticPath)) {
    const content = fs.readFileSync(staticPath, 'utf-8');
    res.setHeader('Content-Type', 'application/javascript');
    res.setHeader('Cache-Control', 'public, max-age=60');
    return res.status(200).send(content);
  }

  // Empty fallback
  res.setHeader('Content-Type', 'application/javascript');
  res.setHeader('Cache-Control', 'no-cache');
  return res.status(200).send(
    'var productsData = { "categories": [], "products": [], "company": {}, "headerFooter": {} };\n' +
    'console.warn("API: No data available");\n'
  );
}

// ── Handler ───────────────────────────────────────────────────
module.exports = async function handler(req, res) {
  try {
    const sql = getDb();
    await ensureTable(sql);

    // CORS preflight
    if (req.method === 'OPTIONS') {
      res.setHeader('Access-Control-Allow-Origin', '*');
      res.setHeader('Access-Control-Allow-Methods', 'GET, POST, OPTIONS');
      res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
      return res.status(200).end();
    }

    if (req.method === 'GET') {
      // If format=js, serve as JavaScript (for frontend script tags)
      if (req.query && req.query.format === 'js') {
        return await serveAsJavaScript(sql, res);
      }
      // Default: return JSON
      const rows = await sql`SELECT data FROM admin_data WHERE id = 1`;
      let data = rows.length > 0 ? rows[0].data : {};

      // Auto-repair: if data is corrupted (empty/non-array products), reseed
      if (isDataCorrupted(data)) {
        console.log('Data corrupted, reseeding from static file');
        // Delete and reseed
        await sql`DELETE FROM admin_data WHERE id = 1`;
        await seedFromStaticFile(sql);
        const newRows = await sql`SELECT data FROM admin_data WHERE id = 1`;
        data = newRows.length > 0 ? newRows[0].data : {};
      }

      // Fallback: if DB data is still empty but a static file exists, use that
      if (isDataCorrupted(data)) {
        const fallbackData = tryReadStaticData();
        if (fallbackData) {
          data = fallbackData;
        }
      }

      return jsonResponse(res, { success: true, data });
    }

    if (req.method === 'POST') {
      const { data, product } = req.body;

      // Single product update (small payload, avoids 4.5MB limit)
      if (product) {
        const rows = await sql`SELECT data FROM admin_data WHERE id = 1`;
        if (rows.length === 0) {
          return jsonResponse(res, { success: false, error: 'No data in DB' }, 404);
        }
        let dbData = rows[0].data;
        if (!Array.isArray(dbData.products)) dbData.products = [];

        // If product has no valid id, auto-generate one
        let pid = parseInt(product.id, 10);
        if (!pid || pid <= 0 || isNaN(pid)) {
          let maxId = 0;
          dbData.products.forEach(p => { if (p && p.id > maxId) maxId = p.id; });
          pid = maxId + 1;
          product.id = pid;
        }

        const idx = dbData.products.findIndex(p => p && p.id === product.id);
        if (idx >= 0) {
          // ★ MERGE: only overwrite fields sent in the payload,
          //          keeping existing fields untouched
          Object.keys(product).forEach(function(k) {
            dbData.products[idx][k] = product[k];
          });
        } else {
          dbData.products.push(product);
        }
        await sql`UPDATE admin_data SET data = ${JSON.stringify(dbData)}::jsonb, updated_at = CURRENT_TIMESTAMP WHERE id = 1`;
        return jsonResponse(res, { success: true, message: 'Product saved', id: product.id });
      }

      // RESET: reseed DB from static file
      if (req.body && req.body._action === 'reset') {
        await sql`DELETE FROM admin_data WHERE id = 1`;
        await seedFromStaticFile(sql);
        return jsonResponse(res, { success: true, message: 'DB reset from static file' });
      }

      // UPLOAD IMAGE: forward to Telegraph (free image host by Telegram)
      if (req.body && req.body._action === 'upload_image') {
        const imageBase64 = req.body.image;
        const filename = req.body.filename || 'upload.jpg';
        if (!imageBase64) {
          return jsonResponse(res, { success: false, error: 'Missing "image" (base64)' }, 400);
        }
        try {
          const imageBuffer = Buffer.from(imageBase64, 'base64');
          const url = await uploadToTelegraph(imageBuffer, filename);
          return jsonResponse(res, { success: true, url });
        } catch (e) {
          return jsonResponse(res, { success: false, error: e.message }, 500);
        }
      }

      // Full data replace
      if (!data) {
        return jsonResponse(res, { success: false, error: 'Missing "data" or "product" in request body' }, 400);
      }
      await sql`
        UPDATE admin_data
        SET data = ${data}::jsonb, updated_at = CURRENT_TIMESTAMP
        WHERE id = 1
      `;
      return jsonResponse(res, { success: true, message: 'Data saved' });
    }

    return jsonResponse(res, { success: false, error: 'Method not allowed' }, 405);
  } catch (error) {
    console.error('Data API error:', error);
    return jsonResponse(res, { success: false, error: error.message }, 500);
  }
};

// ── Upload to Telegraph (Telegram's free image host) ──────────
function uploadToTelegraph(imageBuffer, filename) {
  return new Promise((resolve, reject) => {
    const boundary = '----TelegraphBoundary' + Date.now();
    const header = Buffer.from(
      '--' + boundary + '\r\n' +
      'Content-Disposition: form-data; name="image"; filename="' + filename + '"\r\n' +
      'Content-Type: image/jpeg\r\n\r\n'
    );
    const footer = Buffer.from('\r\n--' + boundary + '--\r\n');
    const body = Buffer.concat([header, imageBuffer, footer]);

    const opts = {
      hostname: 'telegra.ph',
      path: '/upload',
      method: 'POST',
      headers: {
        'Content-Type': 'multipart/form-data; boundary=' + boundary,
        'Content-Length': body.length,
      },
    };

    const req = require('https').request(opts, (res) => {
      let data = '';
      res.on('data', (c) => (data += c));
      res.on('end', () => {
        try {
          const result = JSON.parse(data);
          if (Array.isArray(result) && result[0] && result[0].src) {
            resolve('https://telegra.ph' + result[0].src);
          } else if (result.error) {
            reject(new Error(result.error));
          } else {
            reject(new Error('Unexpected response: ' + data));
          }
        } catch (e) {
          reject(new Error('Parse error: ' + e.message + ' body: ' + data));
        }
      });
    });
    req.on('error', reject);
    req.write(body);
    req.end();
  });
}
