// ============================================================
// products-sync-guard.js
// ------------------------------------------------------------
// 唯一负责"把 products-data.js 写到磁盘"的安全网关。
// admin/index.html 和 admin/product_edit.html 都必须通过这里写盘，
// 不允许各自再实现一份 writeProductsDataToDisk。
//
// 解决的核心问题：
//   之前任何一个页面只要 APP_DATA 里的产品数量因为各种原因
//   （文件夹没连接 / localStorage 被清空 / 换了浏览器 / 磁盘文件
//   读取失败）意外变小，一旦触发"同步到前端网站"或自动保存，
//   就会把这份"缩水"的数据直接覆盖写入 aec-project/products-data.js，
//   造成 256 个产品变成 5 个、且无法恢复的灾难。
//
// 本模块提供两层保护：
//   1) 危险操作拦截：新数据量比历史最高纪录下降超过 20% 时拒绝写入，
//      除非调用方显式传入 force:true（并在 UI 上要求用户二次确认）。
//   2) 自动备份：每次真正写盘前，把磁盘上"旧"的 products-data.js
//      先另存一份到 backups/ 子目录（保留最近 15 份），
//      任何一次写入出问题，都能从 backups/ 里找回。
// ============================================================
(function (global) {
  'use strict';

  var MAX_COUNT_KEY = 'aecAdmin_maxProductCount';
  var BACKUP_KEEP = 15;

  function getLastKnownGoodCount() {
    try {
      var v = localStorage.getItem(MAX_COUNT_KEY);
      return v ? parseInt(v, 10) : 0;
    } catch (e) { return 0; }
  }

  // 只允许历史纪录"越用越大"，不会因为一次异常的小数据而被拉低
  function recordGoodCount(n) {
    try {
      var prev = getLastKnownGoodCount();
      if (n > prev) localStorage.setItem(MAX_COUNT_KEY, String(n));
    } catch (e) { /* ignore */ }
  }

  // 用户在核实过"数量下降是有意为之"（比如手动查重删除了重复产品）之后，
  // 显式确认当前数量就是新的、正确的基准值 —— 与 recordGoodCount 不同，
  // 这里允许把基准值往下调，且必须由用户在 UI 上主动点击确认才会调用，
  // 不会被代码自动触发。
  function setBaseline(n) {
    try {
      localStorage.setItem(MAX_COUNT_KEY, String(n));
      return true;
    } catch (e) {
      return false;
    }
  }

  // 历史基线小于10时不拦截（正常初始建站阶段）；
  // 否则新数量低于历史最高的 80% 视为危险下降
  function isDangerousDrop(newCount) {
    var prev = getLastKnownGoodCount();
    if (prev < 10) return false;
    return newCount < prev * 0.8;
  }

  async function ensureBackupDir(folderHandle) {
    return folderHandle.getDirectoryHandle('backups', { create: true });
  }

  async function writeBackup(folderHandle, jsContent) {
    try {
      var backupDir = await ensureBackupDir(folderHandle);
      var stamp = new Date().toISOString().replace(/[:.]/g, '-');
      var fh = await backupDir.getFileHandle('products-data.' + stamp + '.js', { create: true });
      var w = await fh.createWritable();
      await w.write(jsContent);
      await w.close();

      // 清理旧备份，只保留最近 BACKUP_KEEP 份
      var names = [];
      for await (var entry of backupDir.values()) {
        if (entry.kind === 'file' && /^products-data\..*\.js$/.test(entry.name)) {
          names.push(entry.name);
        }
      }
      names.sort();
      while (names.length > BACKUP_KEEP) {
        var toDelete = names.shift();
        try { await backupDir.removeEntry(toDelete); } catch (e) { /* ignore */ }
      }
    } catch (e) {
      console.warn('[products-sync-guard] 备份失败（不影响本次保存）：', e);
    }
  }

  // data: { categories, products }
  // opts: { force: boolean }
  // 返回 { ok, blocked, reason }
  async function safeWriteProductsData(folderHandle, data, opts) {
    opts = opts || {};
    var force = !!opts.force;
    var newCount = (data.products || []).length;

    if (isDangerousDrop(newCount) && !force) {
      return {
        ok: false,
        blocked: true,
        newCount: newCount,
        lastGoodCount: getLastKnownGoodCount(),
        reason:
          '⚠️ 危险操作已拦截\n\n' +
          '即将写入磁盘的产品数量为 ' + newCount + ' 个，' +
          '而本机记录的历史最高数量为 ' + getLastKnownGoodCount() + ' 个（下降超过 20%）。\n\n' +
          '常见原因：\n' +
          '· 网站文件夹没有正确连接，或选错了文件夹\n' +
          '· 浏览器缓存被清空 / 换了浏览器或电脑打开后台\n' +
          '· products-data.js 文件本身已损坏，读取失败\n\n' +
          '为防止把真实数据永久覆盖掉，本次保存已被自动阻止，磁盘文件未被修改。\n' +
          '请先按照下方指引恢复数据，确认无误后再选择"仍要覆盖"。'
      };
    }

    var js = '// Auto-generated by AEC Admin - ' + new Date().toISOString() + '\n';
    js += 'var productsData = ' + JSON.stringify(data, null, 2) + ';\n';
    js += 'if (typeof window !== "undefined") { window.productsData = productsData; }\n';

    try {
      // 写入前，把磁盘上现有的文件先备份一份
      try {
        var existingHandle = await folderHandle.getFileHandle('products-data.js');
        var existingFile = await existingHandle.getFile();
        var existingText = await existingFile.text();
        if (existingText && existingText.length > 50) {
          await writeBackup(folderHandle, existingText);
        }
      } catch (e) { /* 文件不存在，跳过备份 */ }

      var fileHandle = await folderHandle.getFileHandle('products-data.js', { create: true });
      var writable = await fileHandle.createWritable();
      await writable.write(js);
      await writable.close();

      // 普通情况下基准值只升不降；但如果这次写盘是用户在弹窗里主动确认过的
      // "强制覆盖"，说明用户已经核实过这次的数量下降是有意为之（比如手动查重），
      // 这时应该把新数量当作新的基准值，避免下次操作又被同一个旧基准拦截。
      if (force) {
        setBaseline(newCount);
      } else {
        recordGoodCount(newCount);
      }
      return { ok: true, blocked: false, reason: '', newCount: newCount };
    } catch (e) {
      return { ok: false, blocked: false, reason: '写入失败：' + e.message, newCount: newCount };
    }
  }

  global.ProductsSyncGuard = {
    getLastKnownGoodCount: getLastKnownGoodCount,
    recordGoodCount: recordGoodCount,
    setBaseline: setBaseline,
    isDangerousDrop: isDangerousDrop,
    safeWriteProductsData: safeWriteProductsData
  };
})(window);
